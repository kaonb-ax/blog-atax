          </li><li><i>ls </i>: liste les élément present dans le repertoire courant
          </li><li><i>pwd </i>: affiche le repertroire courant et son arboréscence depuis la racine
          </li><li><i>touch </i>: créé un fichier
          </li><li><i>cat </i>: affiche le contenu d'un fichier, et peut permettre de l'edité de facon très basique
          </li><li><i>mkdir </i>: créé un dossier
          </li><li><i>history </i>: affiche l'historique des commande précedement rentré
          </li><li><i>nano </i>: permet de démarrer l'éditeur nano
       </li></ul>
